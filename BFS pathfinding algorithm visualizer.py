    def update(self,arg):
        if self.checkable==False:
            return 0
        if arg in self.walls:
            arg.setStyleSheet("background-color: rgb(255, 255, 255);")
            self.walls.remove(arg)

        else:
            arg.setStyleSheet("background-color: rgb(131, 131, 131);")
            self.walls.append(arg)
    
    def clear(self):
        if self.checkable==False:
            return 0
        else:
            for i in self.ls:
                for j in i:
                    j.setStyleSheet("background-color: rgb(255, 255, 255);")
            self.walls=[]
    
    def generate(self):
        if self.checkable==False:
            return 0
        self.clear()
        for i in range(83):
            a=random.choice(self.lp)
            while a in self.walls:
                a=random.choice(self.lp)
            self.walls.append(a)

        for i in self.walls:
            i.setStyleSheet("background-color: rgb(131, 131, 131);")
    
    def search(self):
        if self.checkable==False:
            return 0
        self.checkable=False
        ls=[(0,0),]
        want=(12,19)
        x=len(self.ls)
        cs1,cs2=0,0
        ck=set(ls)
        newls=[(0,0)]
        while want not in ls:
            time.sleep(0.2)
            b=[]
            for i in range(cs1,len(ls)):
                new=[(ls[i][0]-1,ls[i][1]),(ls[i][0]+1,ls[i][1]),(ls[i][0],ls[i][1]+1),(ls[i][0],ls[i][1]-1)]
                for z in new:
                    if -1 not in z and x!=z[0] and 20!=z[1] and self.ls[z[0]][z[1]] not in self.walls:
                        b.append(z)
            cs1=len(ls)-1
            self.test=[]
            self.zk=ls[:]
            for i in b:
                if i not in ck:
                    self.test.append(i)
                    ls.append(i)
                    ck.update((i,))
            self.col.emit()
            cs2=len(ls)-1
            if cs1==cs2:
                    self.checkable=True
                    self.suc.emit()
                    return False
        ans=[]
        last=want
        x=ls[::-1][1:]
        for i in x:
            if sorted([abs(last[0]-i[0]),abs(last[1]-i[1])])==[0,1]:
                last=i
                ans.append(i)
        ans=ans[::-1]
        ans.append((12,19))
        self.test=[]
        self.zk=[]
        for i in ans:
            time.sleep(0.1)
            self.ls[i[0]][i[1]].setStyleSheet("background-color: rgb(255, 0, 0);")
        self.checkable=True
        return ans

